          <h2>Our Founder</h2>
          <p>
          What's in a name? Well, for many, their name is a true extension of their being. Take for instance the name, Graziano. It literally means "welcoming" or "pleasing" in Italian. Is it any wonder then, that Graziano Sbroggio has established a reputation for creating truly welcoming and pleasing restaurants?</p>
          <img id="founder-img" src="images/pic-about.jpg" align="right"/>
          <p>His story aptly begins in Italy.<br>
          Born and raised in Italy, Graziano was weaned on the restaurant industry from a young age. His parents owned an authentic trattoria called La Cappellatta in Treviso, Italy. It was there that Graziano developed his passion for cuisine while honing his skills as a restaurateur. By the time he graduated from school, he took over the restaurant and built on the success of his parents. But as successful as he was, ultimately, his destiny would lead him to the United States.<br>
          </p>
          <p>In 1990, Graziano found himself on the sun-drenched shores of South Florida. It was here that he would become the general manager at a popular Miami restaurant called TiramesU. By 1997, he was the managing partner and a co-owner of the establishment. Then, relying on his entrepreneurial know-how and business foresight, he relocated TiramesU to the Lincoln Road strip where the restaurant quickly became one of South Beach's most popular Italian restaurants.<br>
          </p>
          <br>
          <h2>Spris is Born</h2>
          <p>
          With the growing success of TiramesU, Graziano would open up the first Spris restaurant in 1998, just two doors down from TiramesU. With Spris, Graziano brought authentic wood-burning oven pizza to the Miami masses and needless to say, the pizza's super-fresh ingredients, crispy thin crust, and whole wheat and gluten-free options quickly made Spris THE place to get pizza in South Beach. Unlike so many of its competitors, Spris makes pizza the old-fashioned way, using the best ingredients sourced from local providers, just like they do in Italy.<br>
          </p>
          <p>But, simply serving the best pizza in Miami wasn't good enough for Graziano. He wanted to serve the best pizza this side of Italy. So, in 2001 he opened up Spuntino Bakery along with Carlo Donadoni, the executive chef at TiramesU at the time. Using age-old bread making methods learned from Italian artisans, Graziano now had his own personal bakery where all of his pasta, dough, and baked goods could be made fresh every day. From the ground-up, Spris creates their own unique pizza using their own proprietary dough. And, unlike most other pizza joints, Spris lets their dough rest for a full 48 hours before using it. This time-honored secret makes the dough lighter and easier to digest.<br>
          </p>
          <p>Relying on his entrepreneurial spirit, his constant desire for improvement, and his Italian upbringing, Graziano Sbroggio brings consistency, good food, good value, and yes, a welcoming, appreciative attitude to every Spris Artisan Pizza. It is with this philosophy that he made Spris a physical representation of his company's slogan, "We are food people. It's what we do. It's what we love."</p>
          <br>
          <h2>The Team</h2>
          <h3 class="h3-24px-font-size">Andrea Di Giuseppe - Director</h3>
          <p>A founding Director of Spris Artisan Pizza, Andrea Di Giuseppe brings his invaluable 16-year expertise in franchise development.  He currently serves as Rocksolid Granit (USA), Inc.’s President and Chairman of the Board, positions he has held, respectively, April 2004 since and December 2006. Rocksolid Granit is the franchisor for the “Granite Transformations” concept in North America, a worldwide leader in the kitchen and bath remodeling industry. Granite Transformations holds franchisor rights with affiliates in Georgia, California, Utah and Florida. </p>
          <p>Andrea rose through the ranks at Rocksolid Granite (USA) and oversaw the development of the franchise concept throughout the United States as co-owner of Granite Transformations, ranked in Entrepreneur Magazine’s 2015 Top 500 Franchise with over 145 franchises worldwide. The company has its U.S. headquarters in Miramar, Florida and European headquarters in London, UK.<p>
          <p>Andrea has lent his expertise in identifying unique franchise opportunities to the team. His Italian roots and love for food have kept the successful balance between a scalable concept and quality at Spris Artisan Pizza.</p>
